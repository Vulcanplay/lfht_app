
export interface ActionOptions {
    /**
     * Display the action button or not.
     */
    hidden?: boolean;
    /**
     * Optional font size adjustement.
     */
    fontSize?: string;
    /**
     * The action Ionic icon name to display.
     */
    iconName?: string;
    /**
     * A text to display on the action.
     */
    text?: string;
}